# Create lists
List_1 = [1, 2, 3, 4]
print(List_1)

List_2 = [1.2, 2.2, 3.2, 4.5]
print(List_2)

List_3 = ['A', 'B', 'C', 'D']
print(List_3)

List_4 = [True, True, False, False]
print(List_4)

# Manipulate lists
# Add two lists together
print(List_1+List_2)

# Add one item to the list:
List_3.append('E')
print(List_3)

# Remove one item from the list:
List_4.remove(False)
print(List_4)

